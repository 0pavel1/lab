a = int(input("Введите коэффициент a: "))
b = int(input("Введите коэффициент b: "))
c = int(input("Введите коэффициент c: "))
D = (b**2 - 4*a*c)
if D >= 0:
    x1 = (-b + D**0.5)/(a*2)
    x2 = (-b - D**0.5)/(a*2)
    if x2 == x1:
        print(f"Единственный корень: {x1}")
    else:
        print(f"Первый корень: {x1}")
        print(f"Второй корень: {x2}")
else:
    print("Корней нет.")